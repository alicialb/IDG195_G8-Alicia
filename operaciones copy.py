#Operaciones

num1 = input ("Inserte un número: ")
num2 = input ("Inserte otro número: ")

suma = num1 + num2
print ("El total de la suma es: ", suma)

resta = num1 - num2
print ("El total de la resta es: ", resta)

multiplicacion = num1 * num2
print ("El total de la multiplicacion es: ", multiplicacion)

division = num1 / num2
print ("El total de la division es: ", division)

